use bytes::BytesMut;
use lib_garble_rs::OutputLabels;
use lib_garble_rs::{EncodedGarblerInputs, EvaluatorInput, GarbledCircuit};
use rand::distributions::Distribution;
use rand::distributions::Uniform;
use rand::thread_rng;

pub struct EvaluateWrapper {
    garbled: GarbledCircuit,
    encoded_garbler_inputs: EncodedGarblerInputs,
    evaluator_inputs: Vec<EvaluatorInput>,
    width: usize,
    height: usize,
    temp_outputs: Vec<u8>,
    outputs_labels: OutputLabels,
    /// one per "output" (ie len() == circuit.outputs.len())
    /// This is used to avoid alloc in `decoding_internal` during eval
    outputs_bufs: Vec<BytesMut>,
}

impl EvaluateWrapper {
    /// Create a new EvaluateWrapper, to be used later eg
    /// let evaluate_wrapper = ffi::new_evaluate_wrapper(...);
    /// evaluate_wrapper.EvaluateWithInputs(...); etc
    ///
    /// param: pgarbled_buffer can be a FULL, or a STRIPPED circuit
    /// typically in PROD we use STRIPPED ones, but for tests/dev we keep compat with FULL circuits
    /// [in which case] packmsg_buffer can be empty
    pub fn new(pgarbled_buffer: Vec<u8>) -> EvaluateWrapper {
        let (mut garbled, encoded_garbler_inputs) =
            lib_garble_rs::deserialize_for_evaluator(&pgarbled_buffer).unwrap();
        let evaluator_inputs = lib_garble_rs::prepare_evaluator_inputs(&garbled).unwrap();

        let width = garbled
            .config
            .display_config
            .unwrap()
            .width
            .try_into()
            .unwrap();
        let height = garbled
            .config
            .display_config
            .unwrap()
            .height
            .try_into()
            .unwrap();

        let mut outputs_bufs = Vec::new();
        outputs_bufs.resize_with(garbled.num_outputs(), BytesMut::new);

        EvaluateWrapper {
            garbled,
            encoded_garbler_inputs,
            evaluator_inputs,
            width,
            height,
            temp_outputs: vec![0u8; width * height],
            outputs_labels: OutputLabels::new(),
            outputs_bufs,
        }
    }

    /// PROD version
    /// inputs are randomized, outputs are externally given
    /// typically outputs points to some kind of "Texture data"
    pub fn evaluate_with_packmsg(&mut self, outputs: &mut Vec<u8>) {
        // TODO move those to the struct; but not easy b/c not Sync...
        let mut rng = thread_rng();
        let rand_0_1 = Uniform::from(0..=1);

        // for consistency, it SHOULD roughly match the logic in
        // /lib-garble-rs/lib-garble-rs/tests/common/garble_and_eval_utils.rs
        for input in self.evaluator_inputs.iter_mut() {
            *input = rand_0_1.sample(&mut rng);
        }

        // TODO(opt) remove the clone
        let mut encoded_garbler_inputs = self.encoded_garbler_inputs.clone();

        self.garbled
            .eval(
                &mut encoded_garbler_inputs,
                &self.evaluator_inputs,
                outputs,
                &mut self.outputs_labels,
                &mut self.outputs_bufs,
            )
            .unwrap();
    }

    pub fn get_width(&self) -> usize {
        self.width
    }
    pub fn get_height(&self) -> usize {
        self.height
    }
}

