    /// using reference inputs captured using debugger in lib_garble's test_evaluate
    #[test]
    fn test_encrypt_block() {
        let aes = init_aes(18244512439641284263u64, 8886264546983124647u64);

        // input:
        // low: 10919119286214237354
        // high: 12523168985518919269
        let mut low: u64 = 10919119286214237354;
        let mut high: u64 = 12523168985518919269;
        encrypt_block(&aes, &mut low, &mut high);

        // expected
        // low_ = 8990229243945679585;
        // high_ = 12095259187905307864;
        assert_eq!(low, 8990229243945679585u64);
        assert_eq!(high, 12095259187905307864u64);
    }

    // basic adder; version with inputs(NOT packmsg)
    #[test]
    fn test_evaluate_with_inputs() {
        let adder_bytes = include_bytes!("../tests/data/adder.pgarbled.pb.bin");
        let evaluate_wrapper = ffi::new_evaluate_wrapper(adder_bytes.to_vec(), vec![]);

        // bit0,bit1 + carry
        let all_inputs = vec![
            vec![0u8, 0, 0],
            vec![1, 0, 0],
            vec![0, 1, 0],
            vec![1, 1, 0],
            vec![0, 0, 1],
            vec![1, 0, 1],
            vec![0, 1, 1],
            vec![1, 1, 1],
        ];

        let mut outputs: Vec<Vec<u8>> = Vec::with_capacity(all_inputs.len());
        for inputs in all_inputs {
            let cur_outputs = evaluate_wrapper.EvaluateWithInputs(inputs);
            outputs.insert(outputs.len(), cur_outputs);
        }

        let expected_outputs = vec![
            vec![0, 0],
            vec![1, 0],
            vec![1, 0],
            vec![0, 1],
            vec![1, 0],
            vec![0, 1],
            vec![0, 1],
            vec![1, 1],
        ];
        assert_eq!(outputs, expected_outputs);
    }

